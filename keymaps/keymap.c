void openApplication(char* applicationName) {
    SEND_STRING(SS_DELAY(100));
    SEND_STRING(SS_DOWN(X_LGUI));
    SEND_STRING(SS_TAP(X_SPC));
    SEND_STRING(SS_UP(X_LGUI));
    SEND_STRING(SS_DELAY(50));
    SEND_STRING(applicationName);
    SEND_STRING(SS_DELAY(50));
    SEND_STRING(SS_TAP(X_ENT));
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
    case CHROME:
        if (record->event.pressed) {
            openApplication("chr");
        }
        break;
    case IDEA:
        if (record->event.pressed) {
            openApplication("idea");
        }
        break;
    case SLACK:
        if (record->event.pressed) {
            openApplication("slack");
        }
        break;
    case LSFT_T(KC_AMPR):
            if (record->tap.count && record->event.pressed) {
                tap_code16(KC_AMPR); // Send KC_DQUO on tap
                return false;        // Return false to ignore further processing of key
            }
            break;
    case LCTL_T(KC_UNDS):
        if (record->tap.count && record->event.pressed) {
            tap_code16(KC_UNDS); // Send KC_DQUO on tap
            return false;        // Return false to ignore further processing of key
        }
        break;
    case LALT_T(KC_DQUO):
        if (record->tap.count && record->event.pressed) {
            tap_code16(KC_DQUO); // Send KC_DQUO on tap
            return false;        // Return false to ignore further processing of key
        }
        break;
    case LGUI_T(KC_QUOT):
        if (record->tap.count && record->event.pressed) {
            tap_code16(KC_QUOT); // Send KC_DQUO on tap
            return false;        // Return false to ignore further processing of key
        }
        break;
    case LCTL_T(KC_S):
    case KC_RCTL:
        if (record->event.pressed) {
            isSneaking = true;
        } else {
            isSneaking = false;
        }
        break;
    case LT(EXT, KC_SPC):
        if (record->event.pressed) {
            isJumping  = true;
            showedJump = false;
        } else {
            isJumping = false;
        }
        break;
    }
    return true;
};